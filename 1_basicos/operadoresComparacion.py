numA = 18
numB = 5

#1. Mayor que
resultado = numA > numB #>=
print("Mayor que:", resultado)

#2. Menor que
resultado= numA<numB #<=
print("Menor que:", resultado)

#3. Igualdad
resultado = numA==numB
print("Igual que :", resultado)

#4. Distinto 
resultado = numA != numB
print("Distinto que :", resultado)
